const SKIP_FIRST = 2; // пропускаємо перші 2 удари вступу

// ── Псевдорандом з фіксованим seed ───────────────────────────────────
function seededRandom(seed) {
  let s = seed;
  return function() {
    s = (s * 1664525 + 1013904223) & 0xffffffff;
    return (s >>> 0) / 0xffffffff;
  };
}

const rng = seededRandom(77); // інший seed ніж у easy-song (там 42)

// Рандомна струна що не повторює попередню
function randStr(last, rngFn) {
  let s;
  do { s = Math.floor(rngFn() * 5); } while (s === last);
  return s;
}

// ── Генерація паттерну ────────────────────────────────────────────────

const notes = [];
let lastStr = -1;

// 1. ПОЧАТОК — пропускаємо перші 2, решта по 1 ноті рандомно
INTRO_SEC.slice(SKIP_FIRST).forEach(sec => {
  lastStr = randStr(lastStr, rng);
  notes.push({ string: lastStr, time: Math.round(sec * 1000) });
});

// 2. РОЗГІН — по 1 ноті рандомно
BUILDUP_SEC.forEach(sec => {
  lastStr = randStr(lastStr, rng);
  notes.push({ string: lastStr, time: Math.round(sec * 1000) });
});

// 3. ОСНОВНИЙ КАЧ — кожні 2 удари на одній струні, потім 2 на іншій (2+2+2...)
// Логіка: groupStr = поточна струна групи (2 ноти), потім змінюється
let groupStr  = randStr(lastStr, rng);
let groupCount = 0; // скільки нот вже на цій струні у поточній групі

GROOVE_SEC.forEach(sec => {
  if (groupCount >= 2) {
    // Вичерпали 2 ноти — переходимо на іншу струну
    groupStr  = randStr(groupStr, rng);
    groupCount = 0;
  }
  notes.push({ string: groupStr, time: Math.round(sec * 1000) });
  groupCount++;
});

// ── Експорт ───────────────────────────────────────────────────────────
export const MEDIUM_PATTERN = notes;

